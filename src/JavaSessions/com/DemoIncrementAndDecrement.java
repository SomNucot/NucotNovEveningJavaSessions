package JavaSessions.com;

public class DemoIncrementAndDecrement {

	public static void main(String[] args) {
		
		//++ and --
		
		//++-->Increment
		//-->Decrement
		
		//Post Increment
		//Post Decrement
		
		//Pre Increment
		//Pre Decrement
		
		//Post Increment
		
		int i=2;
		int j=i++;
		System.out.println(i);//3
		System.out.println(j);//2
		
		int ii=4;
		int jj=ii++;
		System.out.println(ii);//5
		System.out.println(jj);//4
		
		
		int iii=3;
		int jjj=iii++;
		System.out.println(iii);//4
		System.out.println(jjj);//3
		
		
		int p=-3;
		int q=p++;
		System.out.println(p);//-2
		System.out.println(q);//-3
		
		
		
		
		//Post Decrement
		

		int iiii=2;
		int jjjj=iiii--;
		System.out.println(iiii);//1
		System.out.println(jjjj);//2
		
		
		int iiiii=4;
		int jjjjj=iiiii--;
		System.out.println(iiiii);//3
		System.out.println(jjjjj);//4
		
		
		int pp=-4;
		int qq=pp--;
		System.out.println(pp);//-5
		System.out.println(qq);//-4
		
		
		
		//Pre Increment
		
		int aa=2;
		int bb=++aa;
		System.out.println(aa);//3
		System.out.println(bb);//3
		
		
		int aaa=-4;
		int bbb=++aaa;
		System.out.println(aaa);//-3
		System.out.println(bbb);//-3
		
		//Pre Decrement
		
		int e=2;
		int f=--e;
		System.out.println(e);//1
		System.out.println(f);//1
		
		
		int ee=-10;
		int ff=--ee;
		System.out.println(ee);//-11
		System.out.println(ff);//-11
		
		
		
		
		

	}

}
