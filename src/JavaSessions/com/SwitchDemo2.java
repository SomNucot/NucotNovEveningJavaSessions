package JavaSessions.com;

public class SwitchDemo2 {

	public static void main(String[] args) {

		
		char ch='v';
		
		
		switch(ch)
		{
		
			case 'a':
				System.out.println("The value is a");
			break;
			case 'e':
				System.out.println("The value is e");
			break;
			
			case 'i':
				System.out.println("The value is i");
			break;
				
			case 'o':
				System.out.println("The value is o");
			break;
			
			case 'u':
				System.out.println("The value is u");
			break;

			default:
				System.out.println("Please pass vaild vowel");
				break;
			
			
		}
		
		
		
		System.out.println("Thanks");
		
	}

}
